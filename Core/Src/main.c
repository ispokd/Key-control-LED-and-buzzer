void flow_led(void)
{
	for(int i=0;i<7;i++)
	{
		HAL_GPIO_WritePin(GPIOB,LED_PIN_1<<i,GPIO_PIN_SET);//LED_PIN_1<<是一个位移操作，将LED_PIN_1的值左移i位
		HAL_Delay(500);                                    //这里假设LED_PIN_1是一个常量，表示某个特定的LED引脚编号
		HAL_GPIO_WritePin(GPIOB,LED_PIN_1<<i,GPIO_PIN_RESET);//通过左移操作，可以生成不同的LED引脚编号，从而控制多个LED灯。
  }
}


void STOP_led(void)
{
	for(int i=0;i<7;i++)
	{
		HAL_GPIO_WritePin(GPIOB,LED_PIN_1<<i,GPIO_PIN_SET);//将LED的七个引脚电平拉高，即使七个LED熄灭。
		
  }
}
